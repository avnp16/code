// 2.1	WAP to declare two variable and calculate (sum/sub/mul/div)using this variable

#include <stdio.h>

int main()
{
    int a, b;
    a = 6, b = 9;

    printf("Sum of A & B is :%d \n", a + b);
    printf("Subtraction of A & B is :%d\n", a - b);
    printf("Multiplication of A & B is:%d\n", a * b);
    printf("Dividation of A & B is :%d\n", a / b);

    return 0;
}